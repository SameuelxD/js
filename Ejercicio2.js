const anonima= function(){
    celsius=parseFloat(prompt("Digite los grados celsius a convertir a grados fahrenheit: "))
    fahrenheit= (32 + ((9)*(celsius)/5));
    return `Los ${celsius} celsius a fahrenheit es ${fahrenheit}`
}
